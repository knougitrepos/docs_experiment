<#
.SYNOPSIS
    OpenAI Codex CLI 어댑터. 비대화형 `codex exec` 사용.

.DESCRIPTION
    공식 CLI: `@openai/codex` (npm 설치) 또는 `codex` 바이너리.
    참고: https://github.com/openai/codex

    본 어댑터는 다음 호출을 시도:
        codex exec --model <M> --json "<prompt>"
    reasoning 은 Codex 최신 CLI 규약에 맞춰
        -c model_reasoning_effort="<E>"
    형태로 전달한다.

    - `exec` 은 Codex CLI 의 비대화형 모드.
    - 실제 플래그 이름/형식은 Codex CLI 버전에 따라 달라질 수 있으므로,
      문제가 있으면 -Extra @{ ExtraArgs = '--some-flag val' } 로 주입하거나,
      `custom.ps1` 어댑터를 사용하여 템플릿 커맨드를 직접 지정하세요.

.NOTES
    OPENAI_API_KEY 등 인증은 사용자의 환경변수/CLI 구성에 위임.
#>
param(
    [Parameter(Mandatory)][string]$Workspace,
    [Parameter(Mandatory)][string]$PromptFile,
    [Parameter(Mandatory)][string]$Model,
    [Parameter(Mandatory)][string]$StreamOut,
    [Parameter(Mandatory)][string]$MetaOut,
    [int]$TimeoutSec = 1800,
    [hashtable]$Extra = @{}
)

. "$PSScriptRoot\_common.ps1"

$reasoningEffort = 'default'
if ($Extra.ContainsKey('ReasoningEffort') -and $Extra.ReasoningEffort) {
    $reasoningEffort = [string]$Extra.ReasoningEffort
}
$reasoningFlag = '--reasoning-effort'
if ($Extra.ContainsKey('ReasoningFlag') -and $Extra.ReasoningFlag) {
    $reasoningFlag = [string]$Extra.ReasoningFlag
}

# codex 바이너리가 없으면 에러 대신 meta 기록 후 non-zero exit
$codex = Get-Command 'codex' -ErrorAction SilentlyContinue
$started = Get-Date
if (-not $codex) {
    Write-AdapterLog "codex CLI not found. Install: npm i -g @openai/codex (or equivalent)" -Level ERROR
    '{"event":"adapter_error","message":"codex CLI not found"}' | Set-Content -Path $StreamOut -Encoding UTF8
    $meta = New-AdapterMeta -Agent 'codex' -Model $Model `
        -Started $started -Finished (Get-Date) -ExitCode 127 `
        -PromptFile $PromptFile -StreamOut $StreamOut -Notes 'codex not in PATH'
    $meta['reasoning_effort'] = $reasoningEffort
    Save-AdapterMeta -Meta $meta -MetaOut $MetaOut
    exit 127
}

$prompt = Get-Content $PromptFile -Raw -Encoding UTF8
Write-AdapterLog "codex exec model=$Model reasoning=$reasoningEffort workspace=$Workspace"

# Base args. Workspaces generated by the experiment runner are not git repos.
$argv = @('exec', '--skip-git-repo-check', '--model', $Model, '--json')
if ($reasoningEffort -ne 'default') {
    if ($reasoningFlag -eq '--reasoning-effort') {
        # 현재 codex-cli 는 reasoning 플래그 대신 config override를 사용
        $argv += @('-c', ('model_reasoning_effort="{0}"' -f $reasoningEffort))
    } else {
        # 사용자가 다른 플래그를 명시적으로 준 경우 그대로 사용
        $argv += @($reasoningFlag, $reasoningEffort)
    }
}
# Extra.ExtraArgs 가 공백 구분된 문자열로 주어지면 토큰화해 뒤에 붙임
if ($Extra.ContainsKey('ExtraArgs') -and $Extra.ExtraArgs) {
    $tokens = $Extra.ExtraArgs -split '\s+'
    $argv += $tokens
}
# 마지막에 프롬프트 텍스트(동일 동작을 위해 문자열로 전달)
$argv += $prompt

$invokeFile = 'codex'
$invokeArgv = $argv
$codexPath = $codex.Path
if (-not $codexPath) { $codexPath = $codex.Source }
if ($codexPath -and $codexPath.EndsWith('.ps1', [System.StringComparison]::OrdinalIgnoreCase)) {
    $basedir = Split-Path -Parent $codexPath
    $codexJs = Join-Path $basedir 'node_modules\@openai\codex\bin\codex.js'
    $node = Get-Command 'node.exe' -ErrorAction SilentlyContinue
    if (-not $node) { $node = Get-Command 'node' -ErrorAction SilentlyContinue }
    if ((Test-Path $codexJs) -and $node) {
        $invokeFile = $node.Source
        $invokeArgv = @($codexJs) + $argv
    }
}

$result = Invoke-WithTimeout `
    -FilePath $invokeFile `
    -ArgumentList $invokeArgv `
    -StdoutFile $StreamOut `
    -TimeoutSec $TimeoutSec `
    -WorkingDirectory $Workspace

$finished = Get-Date
if ($result.TimedOut) { Write-AdapterLog "codex TIMED OUT" -Level ERROR }
if ($result.StdErr)   { Write-AdapterLog "stderr:`n$($result.StdErr.Substring(0,[math]::Min(500,$result.StdErr.Length)))" -Level WARN }

$meta = New-AdapterMeta -Agent 'codex' -Model $Model `
    -Started $started -Finished $finished -ExitCode $result.ExitCode `
    -PromptFile $PromptFile -StreamOut $StreamOut `
    -Notes ("timed_out=" + $result.TimedOut + "; reasoning_effort=" + $reasoningEffort)
$meta['reasoning_effort'] = $reasoningEffort
Save-AdapterMeta -Meta $meta -MetaOut $MetaOut

exit $result.ExitCode
